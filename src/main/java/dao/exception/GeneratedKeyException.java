package dao.exception;

public class GeneratedKeyException extends Exception {

    public GeneratedKeyException() {
    }

    public GeneratedKeyException(String message) {
        super(message);
    }

    public GeneratedKeyException(String message, Throwable cause) {
        super(message, cause);
    }

    public GeneratedKeyException(Throwable cause) {
        super(cause);
    }

    public GeneratedKeyException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
